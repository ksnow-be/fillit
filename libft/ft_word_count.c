#include "libft.h"

int		ft_word_count(const char *s, char c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		if ((s[i] == c) && (s[i + 1] != c))
			j++;
		i++;
	}
	if (s[0] != c)
		j++;
	return (j);
}
