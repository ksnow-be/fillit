#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*tmp;

	if (lst || f)
	{
		tmp = lst;
		return (tmp);
	}
	else
		return (NULL);
}
